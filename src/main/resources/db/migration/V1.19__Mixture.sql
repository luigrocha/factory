/* Table: CETMIX                                                */
/*==============================================================*/
CREATE TABLE CETMIX
(
    ID_CETMIX_CODE          INT NOT NULL AUTO_INCREMENT,
    XID_COTORD_CODE         INT NOT NULL,
    CETMIX_NUMBER           INT NOT NULL,
    CETMIX_DOCUMENT_TO      VARCHAR(32) NOT NULL,
    CETMIX_DOCUMENT_BY      VARCHAR(32) NOT NULL,
    CETMIX_DATE             TIMESTAMP NOT NULL ,
    CETMIX_PREPARE          INT NOT NULL,
    CETMIX_TOTAL            DOUBLE NOT NULL,
    CETMIX_OBSERVATION      VARCHAR(256),
    CETMIX_VALID_FROM       TIMESTAMP,
    CETMIX_VALID_TO         TIMESTAMP NULL,
    CETMIX_CREATED_BY       VARCHAR(16),
    CETMIX_UPDATED_BY       VARCHAR(16),
    CETMIX_CREATED_AT       TIMESTAMP,
    CETMIX_UPDATED_AT       TIMESTAMP,
    PRIMARY KEY (ID_CETMIX_CODE)
);

ALTER TABLE CETMIX COMMENT 'MEZCLA';

