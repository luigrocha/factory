    CETMIX_DET_VALID_TO         TIMESTAMP NULL,
    CETMIX_DET_CREATED_BY       VARCHAR(16),
    CETMIX_DET_UPDATED_BY       VARCHAR(16),
    CETMIX_DET_CREATED_AT       TIMESTAMP,
    CETMIX_DET_UPDATED_AT       TIMESTAMP,
    PRIMARY KEY (ID_CETMIX_DET_CODE)
